int Sprite_CheckStatus(void){
	
	int Status = 0;
	int i = 0;
	
	Status |= GameOverFlag;
	Status |= Player.exploded<<1;
	
	for(i=0;i<4;i++){
		if(Enemy[i].life == 1)
			Status |= 0x04;
	}
	
	if(!(Status & 0x04))		// all enemies are dead (new level), so we will preserve player position 
		FirstTime = 0;
	
	return Status;
}

