void Sprite_MissileAttack(void){
	
	if(PlayerMissile[0].life == 0 && (Bunker.life == 0 || Player.x < Bunker.x-10 || Player.x > Bunker.x+10)){
		PlayerMissile[0].x = Player.x+7;
		PlayerMissile[0].y = 35;
		PlayerMissile[0].y_speed = 1;
		PlayerMissile[0].life = 1;
	}	
}

void Sprite_SpecialMissileAttack(void){
	
	if(PlayerMissile[1].life == 0 && PlayerSpecMissile.life == 0 && (Bunker.life == 0 || Player.x < Bunker.x-10 || Player.x > Bunker.x+10)){
	
		PlayerMissile[1].x = Player.x+7;
		PlayerMissile[1].y = 35;
		PlayerMissile[1].y_speed = 1;
		PlayerMissile[1].life = 1;
		
		if(Player.x < 42){
			PlayerSpecMissile.x = Player.x+18;
			PlayerSpecMissile.x_speed = 1;
		}else if(Player.x > 42){
			PlayerSpecMissile.x = Player.x-1;
			PlayerSpecMissile.x_speed = -1;
		}	
		PlayerSpecMissile.y = 35;
		PlayerSpecMissile.y_speed = 1;
		PlayerSpecMissile.life = 1;
	}
}

